a = 10
b = 5

print("Values: a =", a, ", b =", b)

# Equal to (==)
print("\na == b:", a == b)  # False

# Not equal to (!=)
print("a != b:", a != b)    # True

# Greater than (>)
print("a > b:", a > b)      # True

# Less than (<)
print("a < b:", a < b)      # False

# Greater than or equal to (>=)
print("a >= b:", a >= b)    # True

# Less than or equal to (<=)
print("a <= b:", a <= b)    # False
